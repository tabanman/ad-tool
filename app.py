def calculate(gen, media, product_new, ad):
    product_original = PRODUCT_MAP[product_new]
    a = A_score[gen][media]
    
    try:
        b = B_score[media][product_original]
    except KeyError:
        b = 0

    c = C_score[gen][product_original]
    d = D_score[product_original][media][ad]
    
    s = round(a * WEIGHT["A"] + b * WEIGHT["B"] + c * WEIGHT["C"] + d * WEIGHT["D"], 2)

    if s >= 90:
        roi = "超高"
        advice = "预期效果很好，可加大投放预算，作为核心投放渠道/组合！"
    elif 80 <= s < 90:
        roi = "高"
        advice = "预期效果较好，可适度增加预算，重点布局，持续优化投放形式！"
    elif 70 <= s < 80:
        roi = "较高"
        advice = "匹配度较高，但可继续调整！"
    elif 60 <= s < 70:
        roi = "中等"
        advice = "可小预算测试投放，优化适配维度后再评估！"
    elif 50 <= s < 60:
        roi = "低"
        advice = "不建议使用该组合，需大幅调整投放策略！"
    else:
        roi = "极低/负"
        advice = "建议放弃该投放组合，重新规划代际/媒介/商品的匹配！"
    
    return a, b, c, d, s, roi, advice

